def hello_world() -> str:
    """
    Returns a string "Hello, World!".

    :rtype: str
    """
    return "Hello, World!"


def hello_name(name: str) -> str:
    """
    Returns a string "Hello, " + name.

    :param name: A string representing the name of the person to greet.
    :rtype: str
    """
    return "Hello, " + name + "!"
